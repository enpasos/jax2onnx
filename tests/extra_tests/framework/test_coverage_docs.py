def test_component_coverage_docs_have_no_open_missing_entries() -> None:
    failures: list[str] = []
    for relative_path, missing_statuses in COMPONENT_COVERAGE_MISSING_STATUSES.items():
        rows = _table_rows(_read_doc(relative_path))
        missing_rows = [
            f"{name} ({status})"
            for _, name, status in rows
            if status in missing_statuses
        ]
        if missing_rows:
            failures.append(f"{relative_path}: {', '.join(missing_rows)}")

    assert not failures, "Component coverage docs should stay complete:\n" + "\n".join(
        failures
    )


def test_onnx_operator_coverage_doc_summary_matches_tables() -> None:
    text = _read_doc("docs/user_guide/onnx_operator_coverage.md")
    assert "<!-- AUTOGENERATED ONNX OPERATOR TABLE START -->" in text
    assert "<!-- AUTOGENERATED ONNX OPERATOR TABLE END -->" in text
    assert "<!-- AUTOGENERATED ONNX OPERATOR EXTRA START -->" in text
    assert "<!-- AUTOGENERATED ONNX OPERATOR EXTRA END -->" in text

    summary = _snapshot_counts(text)
    main_rows = _onnx_main_rows(text)
    extra_rows = _onnx_extra_rows(text)
    referenced_rows = [row for row in main_rows if row["in_plugins"] == ONNX_CHECK]
    action_buckets = _onnx_action_buckets(text)

    assert main_rows, "ONNX operator coverage has no main table rows"
    assert summary["Operators in index"] == len(main_rows)
    assert summary["Operators referenced in plugins"] == len(referenced_rows)
    assert summary["Extra plugin names not present in index"] == len(extra_rows)
    assert action_buckets, "ONNX operator coverage has no action buckets"
    assert sum(action_buckets.values()) == len(main_rows) - len(referenced_rows)

    expected_coverage = f"{len(referenced_rows) / len(main_rows):.1%}"
    assert f"- Coverage: `{expected_coverage}`" in text

    for row in main_rows:
        has_metadata = row["metadata"] == ONNX_CHECK
        has_lowering = row["lowering"] == ONNX_CHECK
        has_plugins = row["in_plugins"] == ONNX_CHECK
        assert has_plugins == (has_metadata or has_lowering), row["op"]
        assert (row["modules"] != ONNX_EMPTY) == has_plugins, row["op"]
        assert row["next_action"], row["op"]

    for row in extra_rows:
        has_metadata = row["metadata"] == ONNX_CHECK
        has_lowering = row["lowering"] == ONNX_CHECK
        assert has_metadata or has_lowering, row["name"]
        assert row["modules"] != ONNX_EMPTY, row["name"]
        assert row["next_action"], row["name"]


def test_onnx_operator_coverage_has_no_official_metadata_lowering_asymmetry() -> None:
    text = _read_doc("docs/user_guide/onnx_operator_coverage.md")
    asymmetries = [
        f"{row['op']}: metadata={row['metadata']} lowering={row['lowering']}"
        for row in _onnx_main_rows(text)
        if row["metadata"] != row["lowering"]
    ]

    assert not asymmetries, (
        "Official ONNX operator coverage should not contain metadata/lowering "
        "asymmetries:\n" + "\n".join(asymmetries)
    )
