# tests/extra_tests/framework/test_coverage_docs.py

"""Guard generated component coverage docs against internal drift."""

from __future__ import annotations

from collections import Counter
from pathlib import Path
import re
from typing import Final

REPO_ROOT = Path(__file__).resolve().parents[3]

SNAPSHOT_RE: Final = re.compile(r"^- (?P<label>.+?): `(?P<count>\d+)`$")
ROW_RE: Final = re.compile(
    r"^\| \[(?P<check>[ x])\] \| `(?P<name>[^`]+)` \| "
    r"`(?P<status>[^`]+)` \| `(?P<modules>[^`]*)` \| (?P<note>.*) \|$"
)
ONNX_MAIN_ROW_RE: Final = re.compile(
    r"^\| \[(?P<op>[A-Za-z0-9_]+)\]"
    r"\(https://onnx\.ai/onnx/operators/onnx__[A-Za-z0-9_]+\.html\) \| "
    r"(?P<in_plugins>[^|]+) \| (?P<metadata>[^|]+) \| "
    r"(?P<lowering>[^|]+) \| (?P<modules>[^|]+) \| "
    r"(?P<candidates>[^|]+) \| (?P<next_action>[^|]+) \|$"
)
ONNX_EXTRA_ROW_RE: Final = re.compile(
    r"^\| `(?P<name>[^`]+)` \| (?P<metadata>[^|]+) \| "
    r"(?P<lowering>[^|]+) \| (?P<modules>[^|]+) \| "
    r"(?P<candidates>[^|]+) \| (?P<next_action>[^|]+) \|$"
)
ONNX_ACTION_BUCKET_RE: Final = re.compile(r"^  - (?P<label>[^:]+): `(?P<count>\d+)`$")
ONNX_CHECK: Final = "\u2705"
ONNX_EMPTY: Final = "\u2796"
COMPONENT_COVERAGE_MISSING_STATUSES: Final = {
    "docs/user_guide/jax_lax_coverage.md": {"missing", "missing_linalg"},
    "docs/user_guide/jax_numpy_coverage.md": {"missing"},
    "docs/user_guide/flax_api_coverage.md": {"missing"},
    "docs/user_guide/equinox_nn_coverage.md": {"missing"},
}


def _read_doc(relative_path: str) -> str:
    return (REPO_ROOT / relative_path).read_text(encoding="utf-8")


def _snapshot_counts(text: str) -> dict[str, int]:
    counts: dict[str, int] = {}
    for line in text.splitlines():
        match = SNAPSHOT_RE.match(line)
        if match is not None:
            counts[match.group("label")] = int(match.group("count"))
    return counts


def _table_rows(text: str) -> list[tuple[str, str, str]]:
    rows: list[tuple[str, str, str]] = []
    for line in text.splitlines():
        match = ROW_RE.match(line)
        if match is not None:
            rows.append(
                (
                    match.group("check"),
                    match.group("name"),
                    match.group("status"),
                )
            )
    return rows


def _onnx_main_rows(text: str) -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    for line in text.splitlines():
        match = ONNX_MAIN_ROW_RE.match(line)
        if match is not None:
            rows.append(
                {key: value.strip() for key, value in match.groupdict().items()}
            )
    return rows


def _onnx_extra_rows(text: str) -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    in_extra_section = False
    for line in text.splitlines():
        if line == "<!-- AUTOGENERATED ONNX OPERATOR EXTRA START -->":
            in_extra_section = True
            continue
        if line == "<!-- AUTOGENERATED ONNX OPERATOR EXTRA END -->":
            break
        if not in_extra_section:
            continue
        match = ONNX_EXTRA_ROW_RE.match(line)
        if match is not None:
            rows.append(
                {key: value.strip() for key, value in match.groupdict().items()}
            )
    return rows


def _onnx_action_buckets(text: str) -> dict[str, int]:
    buckets: dict[str, int] = {}
    for line in text.splitlines():
        match = ONNX_ACTION_BUCKET_RE.match(line)
        if match is not None:
            buckets[match.group("label")] = int(match.group("count"))
    return buckets


def _assert_doc_counts(
    *,
    relative_path: str,
    status_labels: dict[str, str],
    checked_statuses: set[str],
    unchecked_statuses: set[str],
    total_label: str,
    derived_counts: dict[str, int] | None = None,
) -> None:
    text = _read_doc(relative_path)
    assert "## Snapshot" in text
    assert "## Full Checklist" in text

    snapshot = _snapshot_counts(text)
    rows = _table_rows(text)
    assert rows, f"{relative_path} has no coverage table rows"

    statuses = Counter(status for _, _, status in rows)
    expected_statuses = (
        set(status_labels.values()) | checked_statuses | unchecked_statuses
    )
    assert set(statuses) <= expected_statuses
    assert snapshot[total_label] == len(rows)

    for label, status in status_labels.items():
        assert snapshot[label] == statuses[status]

    for label, expected in (derived_counts or {}).items():
        assert snapshot[label] == expected

    for checkbox, name, status in rows:
        if status in checked_statuses:
            assert checkbox == "x", f"{relative_path}: {name} should be checked"
        elif status in unchecked_statuses:
            assert checkbox == " ", f"{relative_path}: {name} should be unchecked"
        else:
            raise AssertionError(
                f"{relative_path}: unknown status {status!r} for {name}"
            )


